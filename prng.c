void print_bit32_raw(bit*field){
    for(int bits=0;bits<32;bits+=8){
        uint8_t byte = 0;
        for(int i=0;i<8;i++){
              byte |= field[bits + i] << (bits + i);
        }
        putc(byte, stdout);
    }
}

